count = 1
print("Count from 1 to 3.")
while count < 4:
    print(count)
    count = count + 1

print("_______")

count = 5
print("Count from 5 to 10.")
while count < 11:
    print(count)
    count = count + 1

print("_______")

count = 0
print("Skip count by 5's from 0 to 20.")
while count < 21:
    print(count)
    count = count + 5

print("_______")

count = 5
print("Count backwards from 5 to 1.")
while count > 0:
    print(count)
    count = count - 1
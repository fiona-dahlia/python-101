import turtle

win = turtle.Screen()
win.setup(800, 600)
win.title("Challenge 14")

t = turtle.Turtle()
t.speed("fast")
t.pensize(1)
t.penup()

t.fillcolor("brown")
t.goto(0, 200)
t.begin_fill()
t.goto(120, 60)
t.goto(-120, 60)
t.end_fill()

t.fillcolor("yellow")
t.begin_fill()
t.goto(-110, 60)
t.goto(-110, -100)
t.goto(110, -100)
t.goto(110, 60)
t.goto(-110, 60)
t.end_fill()

t.fillcolor("white")
t.begin_fill()
t.goto(-80, 30)
t.goto(-30, 30)
t.goto(-30,-10)
t.goto(-80, -10)
t.goto(-80, 30)
t.end_fill()
t.fillcolor("white")
t.begin_fill()
t.goto(30, 30)
t.goto(80, 30)
t.goto(80,-10)
t.goto(30, -10)
t.goto(30, 30)
t.end_fill()

t.fillcolor("brown")
t.begin_fill()
t.goto(-20, -40)
t.goto(20, -40)
t.goto(20, -100)
t.goto(-20, -100)
t.goto(-20, -40)
t.end_fill()

t.goto(180, -50)
t.fillcolor("green")
t.begin_fill()
t.circle(50)
t.end_fill()

t.fillcolor("brown")
t.begin_fill()
t.goto(170, -50)
t.forward(20)
t.right(90)
t.forward(80)
t.right(90)
t.forward(20)
t.right(90)
t.forward(80)
t.end_fill()